#include "get_next_line.h"
#include <fcntl.h>
#include <unistd.h>

int	main(void)
{
	int		fd;
	char *line;
	fd = open("test.txt", O_RDONLY);
	if (fd == -1)
	{
		printf("Error");
		return (1);
	}
	line = get_next_line(fd);
	while (line)
	{
		printf("%s", line);
		free(line);
		line = get_next_line(fd);
	}
	close(fd);
	return(0);
}
